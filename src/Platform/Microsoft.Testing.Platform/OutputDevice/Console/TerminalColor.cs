    /// <summary>
    /// Green.
    /// </summary>
    DarkGreen = 32,

    /// <summary>
    /// Yellow.
    /// </summary>
    DarkYellow = 33,

    /// <summary>
    /// Blue.
    /// </summary>
    DarkBlue = 34,

    /// <summary>
    /// Magenta.
    /// </summary>
    DarkMagenta = 35,

    /// <summary>
    /// Cyan.
    /// </summary>
    DarkCyan = 36,

    /// <summary>
    /// White.
    /// </summary>
    DarkWhite = 37,

    /// <summary>
    /// Default.
    /// </summary>
    Default = 39,

    /// <summary>
    /// Gray.
    /// </summary>
    Gray = 90,

    /// <summary>
    /// Red.
    /// </summary>
    Red = 91,

    /// <summary>
    /// Green.
    /// </summary>
    Green = 92,

    /// <summary>
    /// Yellow.
    /// </summary>
