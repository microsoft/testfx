    /// Blue.
    /// </summary>
    Blue = 94,

    /// <summary>
    /// Magenta.
    /// </summary>
    Magenta = 95,

    /// <summary>
    /// Cyan.
    /// </summary>
    Cyan = 96,

    /// <summary>
    /// White.
    /// </summary>
    White = 97,
}
